library(Random)
library(tinytest)
set.seed(1234)
vals <- runif(10000)
vals_own <- Random::MT(1234, 10000)
expect_equal(sum(vals == vals_own), 10000)

set.seed(9999)
vals <- runif(10000)
vals_own <- Random::MT(9999, 10000)
expect_equal(sum(vals == vals_own), 10000)

